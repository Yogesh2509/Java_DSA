public class relationalOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        int x = 20;
        int y = 20;
        System.out.println(a==b);
        System.out.println(x==y);      //equals too
        System.out.println(a!=b);     //not equals too
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(x<=y);
    }
}
