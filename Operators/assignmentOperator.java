public class assignmentOperator{
public static void main(String[] args) {
    int a = 10;
    // a = a + 10;
    a += 10;    // this line is same as the upper commented line ..it is to show the use of assignment operator
    System.out.println(a);

    int b = 5;
    //b = b * 5;
    b *= 5;
    System.out.println(b);
}
}