public class logicalOperators {
    public static void main(String[] args) {
        System.out.println((5 > 2) && (9 > 7));   //logical and ,,if both the statements are true than the answer will also be true

        System.out.println((5 > 7 ) || ( 3 > 2));  // logical Or ,,,if any one statement is true than the answer will be true itself

        System.out.println(!(3>2));      //logical NOT ,,,if the answer is true it will reverse it and make it false and vice versa
    }
}
